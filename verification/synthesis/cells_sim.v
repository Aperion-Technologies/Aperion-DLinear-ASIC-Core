// verification/synthesis/cells_sim.v
// Behavioral models for gate-level simulation
//
// These match the cells defined in cells_sim.lib
// Used by Verilator/Icarus for functional gate-level simulation

`timescale 1ns/1ps

// =============================================================================
// Inverter
// =============================================================================
module NOT (
    input  A,
    output Y
);
    assign #0.1 Y = ~A;
endmodule

// =============================================================================
// Buffer
// =============================================================================
module BUF (
    input  A,
    output Y
);
    assign #0.1 Y = A;
endmodule

// =============================================================================
// 2-input AND
// =============================================================================
module AND2 (
    input  A,
    input  B,
    output Y
);
    assign #0.15 Y = A & B;
endmodule

// =============================================================================
// 2-input OR
// =============================================================================
module OR2 (
    input  A,
    input  B,
    output Y
);
    assign #0.15 Y = A | B;
endmodule

// =============================================================================
// 2-input NAND
// =============================================================================
module NAND2 (
    input  A,
    input  B,
    output Y
);
    assign #0.12 Y = ~(A & B);
endmodule

// =============================================================================
// 2-input NOR
// =============================================================================
module NOR2 (
    input  A,
    input  B,
    output Y
);
    assign #0.12 Y = ~(A | B);
endmodule

// =============================================================================
// 2-input XOR
// =============================================================================
module XOR2 (
    input  A,
    input  B,
    output Y
);
    assign #0.2 Y = A ^ B;
endmodule

// =============================================================================
// 2:1 MUX
// =============================================================================
module MUX2 (
    input  A,
    input  B,
    input  S,
    output Y
);
    assign #0.2 Y = S ? B : A;
endmodule

// =============================================================================
// D Flip-Flop (positive edge)
// =============================================================================
module DFF_P (
    input  CLK,
    input  D,
    output reg Q
);
    always @(posedge CLK) begin
        Q <= #0.3 D;
    end
endmodule

// =============================================================================
// D Flip-Flop with sync reset (active high)
// =============================================================================
module DFF_PR (
    input  CLK,
    input  D,
    input  R,
    output reg Q
);
    always @(posedge CLK) begin
        if (R)
            Q <= #0.35 1'b0;
        else
            Q <= #0.35 D;
    end
endmodule

// =============================================================================
// D Flip-Flop with async reset (active high)
// =============================================================================
module DFF_PAR (
    input  CLK,
    input  D,
    input  R,
    output reg Q
);
    always @(posedge CLK or posedge R) begin
        if (R)
            Q <= 1'b0;
        else
            Q <= #0.3 D;
    end
endmodule

// =============================================================================
// 3-input AND
// =============================================================================
module AND3 (
    input  A,
    input  B,
    input  C,
    output Y
);
    assign #0.2 Y = A & B & C;
endmodule

// =============================================================================
// 3-input OR
// =============================================================================
module OR3 (
    input  A,
    input  B,
    input  C,
    output Y
);
    assign #0.2 Y = A | B | C;
endmodule

// =============================================================================
// 4-input AND
// =============================================================================
module AND4 (
    input  A,
    input  B,
    input  C,
    input  D,
    output Y
);
    assign #0.25 Y = A & B & C & D;
endmodule

// =============================================================================
// 4-input OR
// =============================================================================
module OR4 (
    input  A,
    input  B,
    input  C,
    input  D,
    output Y
);
    assign #0.25 Y = A | B | C | D;
endmodule

// =============================================================================
// AND-OR-INVERT (AOI21)
// =============================================================================
module AOI21 (
    input  A1,
    input  A2,
    input  B,
    output Y
);
    assign #0.15 Y = ~((A1 & A2) | B);
endmodule

// =============================================================================
// OR-AND-INVERT (OAI21)
// =============================================================================
module OAI21 (
    input  A1,
    input  A2,
    input  B,
    output Y
);
    assign #0.15 Y = ~((A1 | A2) & B);
endmodule
